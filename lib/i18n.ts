export function translate(locale: Locale, key: string, vars?: Record<string, string | number>) {
  const template = translations[locale][key] ?? translations.zh[key] ?? key;
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, token) => String(vars[token] ?? `{${token}}`));
}

export function getEventLabel(locale: Locale, type: string) {
  return translate(locale, `event.${type}`);
}

export function getSkinToneLabel(locale: Locale, tone: "fair" | "tan" | "dark") {
  return translate(locale, `costume.skin.${tone}`);
}

export function getAnimalLabel(locale: Locale, animal: "tiger" | "rabbit" | "panda") {
  return translate(locale, `costume.animal.${animal}`);
}

export function getSideLabel(locale: Locale, side: "left" | "right" | "top" | "bottom") {
  return translate(locale, `side.${side}`);
}
